                              <h2>MANDIR HISTORY</h2>
                              <p>Hindu Sanatan Mandir has grown in its reach and significance from a very humble beginning. The mandir was conceptualised when Devotees in Buskerud, Akershus (now Viken) & Telemark and Vestfold realised that commuting to the already existing temple in Oslo on a daily basis was not a feasible option in the long run. In addition, as the Hindu community in the region grew and flourished, the need for a place of worship where the community could gather and pray as a whole was realised.</p>
                              <p>This gave birth to the idea of the Hindu Sanatan Mandir in Drammen, although, not without challenges. A project of such proportion required a large amount of funds and resources. However, the founders of the Mandir took up this challenge and decided to go ahead with pooling the resources for the planned endeavours. The devoted Hindu community contributed in every small way they could for this noble cause and in the year 2002, the Mandir was registered as an independent non-profit entity.</p>
                              <p>The next challenge was to find an appropriate location for the Mandir, focusing on both, the ease of accessibility and availability of adequate space for community worship. The search for the right location was a daunting task for the founders, until 2004 when a suitable site was finally identified. Landfalløya 109 appeared perfect in every way – centrally located and spacious enough to accommodate the devotees and their families for worship, 'langar' and other activities. This proved a huge milestone for the Hindu community in inching closer towards realising their dream– of creating a place of worship where Hinduism could be promoted and practiced freely by everyone. Next few years marked the emphasis on modifications and improvements to the existing building, to adapt it to meet the needs of the temple within the limited available resources. </p>
                              <p>The next objective in process of establishing the mandir was ‘Moorti Sthapna’ for making it a symbol of devotion and worship for all the dedicated devotees. 26th January 2013 marked another milestone in the history of the Hindu Sanatan Mandir. It was on this day that ‘Moorti Sthapna’ was finally performed on a grand scale with participation of Pandits and religious dignitaries from India and across the world. Since then, the devotees of Hindu Sanatan Mandir have put in great efforts in maintaining and running the Mandir, and hope to grow with their love, dedication and devotion towards the almighty.</p>
                              <p>Today Hindu Sanatan Mandir stands with a commitment to help & serve Indian community in Norway and share values of Sanatan Dharma including honesty, purity, goodwill, mercy, patience, forbearance, self-restraint, generosity and asceticism. We sincerely hope that with the divine grace and generosity of our community, we will succeed in all future endeavours and hence be able to preserve the origins, facts and beliefs of the Hindu Sanatan Mandir for our future generations.</p>
